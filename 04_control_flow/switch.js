
const month = 3 

switch (month) {
    case 1:
        console.log("January")
        break;
    case 2:
        console.log("febuary")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("april")
        break;
    case 5:
        console.log("may")
        break;
    case 6:
        console.log("june")
        break;
    case 7:
        console.log("july")
        break;

    default:
        console.log("December")
        break;
}